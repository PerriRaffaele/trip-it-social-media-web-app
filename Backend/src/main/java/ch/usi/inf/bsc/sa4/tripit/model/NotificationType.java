package ch.usi.inf.bsc.sa4.tripit.model;

/** Represents the different possible types of notification */
public enum NotificationType {
  /** Represents a smiley face reaction */
  SMILY_FACE,
  /** Represents an angry face reaction */
  ANGRY_FACE,
  /** Represents a sad face reaction */
  SAD_FACE,
  /** Represents a friend request notification */
  FRIEND_REQUEST,
  /** Represents an accepted friend request notification */
  ACCEPTED_FRIEND_REQUEST,
  /** Represents a like notification */
  LIKE
}
